#include "libft.h"

void	*ft_memcpy(void *in, const void *from, size_t n)
{
	int i;

	i = 0;
	while (n-- > 0)
	{
		((char *)in)[i] = ((char *)from)[i];
		i++;
	}
	return (in);
}
