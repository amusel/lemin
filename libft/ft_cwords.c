#include "libft.h"

int		ft_cwords(char const *s, char c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (s[0] != c && ft_strlen(s) != 0)
		j++;
	while (s[i])
	{
		if (s[i] == c && s[i + 1] != c && s[i + 1] != 0)
			j++;
		i++;
	}
	return (j);
}
